import streamlit as st
import pickle
import pandas as pd
import numpy as np

# Load files
model = pickle.load(open("knn_laptop_model.pkl", "rb"))
scaler = pickle.load(open("scaler.pkl", "rb"))
features = pickle.load(open("features.pkl", "rb"))

st.set_page_config(page_title="Laptop Price Predictor")

st.title("💻 Laptop Price Prediction")

st.write("Enter Laptop Specifications")

company = st.selectbox(
    "Company",
    ["Dell","HP","Lenovo","Asus","Acer","Apple","MSI"]
)

typename = st.selectbox(
    "Type",
    ["Notebook","Gaming","Ultrabook","2 in 1 Convertible"]
)

ram = st.slider(
    "RAM (GB)",
    2,64,8
)

weight = st.number_input(
    "Weight (kg)",
    0.5,5.0,2.0
)

inches = st.slider(
    "Screen Size",
    10.0,20.0,15.6
)

touchscreen = st.selectbox(
    "Touchscreen",
    [0,1]
)

ips = st.selectbox(
    "IPS Display",
    [0,1]
)

ppi = st.number_input(
    "PPI",
    50.0,400.0,140.0
)

ssd = st.number_input(
    "SSD Capacity",
    0,2000,256
)

hdd = st.number_input(
    "HDD Capacity",
    0,2000,0
)

cpu_brand = st.selectbox(
    "CPU Brand",
    ["Intel","AMD"]
)

gpu_brand = st.selectbox(
    "GPU Brand",
    ["Intel","Nvidia","AMD"]
)

opsys = st.selectbox(
    "Operating System",
    ["Windows","Mac","Linux","No OS"]
)

# Encoding values

company_dict = {
    "Dell":0,
    "HP":1,
    "Lenovo":2,
    "Asus":3,
    "Acer":4,
    "Apple":5,
    "MSI":6
}

type_dict = {
    "Notebook":0,
    "Gaming":1,
    "Ultrabook":2,
    "2 in 1 Convertible":3
}

cpu_dict = {
    "Intel":0,
    "AMD":1
}

gpu_dict = {
    "Intel":0,
    "Nvidia":1,
    "AMD":2
}

os_dict = {
    "Windows":0,
    "Mac":1,
    "Linux":2,
    "No OS":3
}

if st.button("Predict Price"):

    data = np.array([[
        company_dict[company],
        type_dict[typename],
        inches,
        ram,
        opsys,
        weight,
        ssd,
        hdd,
        touchscreen,
        ips,
        ppi,
        cpu_dict[cpu_brand],
        gpu_dict[gpu_brand]
    ]])

    data_scaled = scaler.transform(data)

    prediction = model.predict(data_scaled)

    st.success(
        f"Predicted Laptop Price: €{prediction[0]:.2f}"
    )